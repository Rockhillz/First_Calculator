// Addition of numbers 
function addNums(num1, num2) {
    addTotal = num1 + num2;
    console.log(num1 + " + " + num2 + " = " + addTotal);
}

addNums(20, 30);

// Subtraction of Number 
function subtractNums(num1, num2) {
    subTotal = num1 - num2;
    console.log(num1 + " - " + num2 + " = " + subTotal);
}

subtractNums( 900, 200);

// Multiplication of Numbers 
function multiNums(num1, num2) {
    multiTotal = num1 * num2;
    console.log(num1 + " * " + num2 + " = " + multiTotal);
}

multiNums(80, 2);

// Division of Numbers 
function divNums(num1, num2) {
    divTotal = num1 / num2;
    console.log(num1 + " / " + num2 + " = " + divTotal);
}

divNums (70, 89)